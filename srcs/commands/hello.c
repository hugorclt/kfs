#include "vga.h"
#include "printk.h"

void	cmd_hello(void)
{
	vga_set_fg_color(VGA_COLOR_WHITE);
	vga_set_bg_color(VGA_COLOR_BLACK);
	
	char *ascii_art = "\n              ,--------------------------,\n              |  /---------------------\\ |\n              | |                       | |\n              | |     Hello 42          | |\n              | |                       | |\n              | |  Kernel From Scratch  | |\n              | |                       | |\n              |  \\_____________________/ |\n              |___________________________|\n            ,---\\_____     []     _______/------,\n          /         /______________\\           /|\n        /___________________________________ /  | ___\n        |                                   |   |    )\n        |  _ _ _                 [-------]  |   |   (\n        |  o o o                 [-------]  |  /    _)_\n        |__________________________________ |/     /  /\n    /-------------------------------------/|      ( )/\n  /-/-/-/-/-/-/-/-/-/-/-/-/-/-/-/-/-/-/-/ /\n/-/-/-/-/-/-/-/-/-/-/-/-/-/-/-/-/-/-/-/ /\n~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~";

	printk(ascii_art);
	printk("\nby ");
	vga_set_fg_color(VGA_COLOR_LIGHT_RED);
	printk("Hugo");
	vga_set_fg_color(VGA_COLOR_WHITE);
	printk(" and ");
	vga_set_fg_color(VGA_COLOR_LIGHT_CYAN);
	printk("Adrian");
	vga_set_fg_color(VGA_COLOR_WHITE);
	vga_set_bg_color(VGA_COLOR_BLACK);
	printk(" \n\n");
}

